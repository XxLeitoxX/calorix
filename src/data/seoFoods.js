const bySlug = new Map()
for (const food of foodsSEO) {
  bySlug.set(food.slug, food)
  bySlug.set(food.slug_es, food)
}

export function getFoodBySlug(slug) {
  return bySlug.get(String(slug || '').toLowerCase()) || null
}

export function getRelatedFoods(food, max = 5) {
  if (!food) return []
  return foodsSEO
    .filter((f) => f.category === food.category && f.slug !== food.slug)
    .slice(0, max)
}

export function getPopularFoods(max = 10) {
  const featuredOrder = ['rice', 'chicken', 'beef', 'fish', 'egg', 'bread', 'avocado', 'broccoli']
  const featured = featuredOrder
    .map((slug) => foodsSEO.find((f) => f.slug === slug))
    .filter(Boolean)

  if (featured.length >= max) return featured.slice(0, max)

  const rest = foodsSEO.filter((f) => !featuredOrder.includes(f.slug))
  return [...featured, ...rest].slice(0, max)
}

export function getFoodFallbackMacros(food) {
  return DEFAULT_MACROS[food?.category] || DEFAULT_MACROS.carbs
}
